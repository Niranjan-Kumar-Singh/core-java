package com.corejava.oops.module2_inheritance;

/*
 * Parent class
 * This class represents the base class in an Is-A relationship.
 */
public class Parent {

	// Parent class method
	public void property() {
		System.out.println("Cash + Land + Gold");
	}

	// Method that can be overridden by child class
	public void marry() {
		System.out.println("Subha Laxmi");
	}
}